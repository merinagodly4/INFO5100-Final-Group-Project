    private void populateOrganizationComboBox() {
        cmbOrganization.removeAllItems();
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            cmbOrganization.addItem(org);
        }
        cmbOrganization.setSelectedItem(organization);
    }

    private void populateEmployeeComboBox() {
        cmbEmployee.removeAllItems();
        for (Employee emp : organization.getEmployeeDirectory().getEmployeeList()) {
            cmbEmployee.addItem(emp);
        }
        cmbEmployee.setSelectedItem(userAccount.getEmployee());
    }

    private void populateRoleComboBox() {
        cmbRoles.removeAllItems();
        for (Role role : organization.getSupportedRole()) {
            cmbRoles.addItem(role);
        }
        cmbRoles.setSelectedItem(userAccount.getRole());
    }

    private void populateFields() {
        txtUserName.setText(userAccount.getUsername());
        txtPassword.setText(userAccount.getPassword());
    }

    private void configureMode() {
        // Organization and employee are always read-only
        cmbOrganization.setEnabled(false);
        cmbEmployee.setEnabled(false);

        if (editable) {
            lblCreateUser.setText("Update User Account");
            txtUserName.setEditable(true);
            txtPassword.setEditable(true);
            cmbRoles.setEnabled(true);
            btnUpdate.setVisible(true);
        } else {
            lblCreateUser.setText("View User Account");
            txtUserName.setEditable(false);
            txtPassword.setEditable(false);
            cmbRoles.setEnabled(false);
            btnUpdate.setVisible(false);
        }
    }

