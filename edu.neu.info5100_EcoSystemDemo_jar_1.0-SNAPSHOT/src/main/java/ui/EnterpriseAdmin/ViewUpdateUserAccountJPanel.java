    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String newUsername = txtUserName.getText().trim();
        String newPassword = txtPassword.getText().trim();
        Role newRole = (Role) cmbRoles.getSelectedItem();

        if (newUsername.isEmpty() || newPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Username and password cannot be empty.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        userAccount.setUsername(newUsername);
        userAccount.setPassword(newPassword);
        userAccount.setRole(newRole);

        JOptionPane.showMessageDialog(this,
                "User account updated successfully.", "Updated", JOptionPane.INFORMATION_MESSAGE);

        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);

    }//GEN-LAST:event_btnUpdateActionPerformed

