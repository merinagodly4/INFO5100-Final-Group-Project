    private void btnRequestMarketingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestMarketingActionPerformed
        // TODO add your handling code here:
         // Prompt the analyst for a description of the price change
        String msg = JOptionPane.showInputDialog(
                this,
                "Enter marketing request details",
                "Request",
                JOptionPane.PLAIN_MESSAGE
        );

        if (msg == null) {
            // user cancelled
            return;
        }
        msg = msg.trim();
        if (msg.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a message.");
            return;
        }

        // Create and send the request
        MarketingRequest request = new MarketingRequest();
        request.setMessage(msg);        // base WorkRequest message
        request.setTestResult(msg);     // reuse your existing field if needed
        request.setSender(userAccount);
        request.setStatus("Sent");

        // Add to Supplier Pricing org queue and (optionally) to sender's queue
        supplierDataAnalyticsOrganization.getWorkQueue().getWorkRequestList().add(request);
        userAccount.getWorkQueue().getWorkRequestList().add(request);

        JOptionPane.showMessageDialog(this, "Marketing request sent");
        populateTable();

    }//GEN-LAST:event_btnRequestMarketingActionPerformed

