     private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable2.getModel();
        model.setRowCount(0);

        for (WorkRequest wr : supplierDataAnalyticsOrganization.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof MarketingRequest) {
                Object[] row = new Object[2];
                row[0] = wr;                                 // will call toString() on PriceChangeRequest
                row[1] = wr.getStatus();
                model.addRow(row);
            }
        }
    }


